using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Sharpmine.Gen
{

    [Generator]
    public class ServerboundPacketRegistryGenerator : IIncrementalGenerator
    {

        private const string AttributeNamespace = "Sharpmine.Server.Packets";

        private const string AttributeClassName = "PacketAttribute";

        private static readonly DiagnosticDescriptor DuplicateIdError = new DiagnosticDescriptor(
            id: "PKT001",
            title: "Duplicate Packet ID",
            messageFormat: "ID '0x{0:X2}' is already used by '{1}'",
            category: "Usage",
            defaultSeverity: DiagnosticSeverity.Error,
            isEnabledByDefault: true
        );

        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var provider = context.SyntaxProvider.ForAttributeWithMetadataName(
                $"{AttributeNamespace}.{AttributeClassName}",
                predicate: (node, _) => node is ClassDeclarationSyntax,
                transform: (ctx, _) => ctx
            );

            context.RegisterSourceOutput(provider.Collect(), Execute);
        }

        private static void Execute(
            SourceProductionContext context,
            ImmutableArray<GeneratorAttributeSyntaxContext> packets
        )
        {
            if (packets.IsDefaultOrEmpty)
            {
                return;
            }

            var sb = new StringBuilder();
            var usedIds = new Dictionary<int, INamedTypeSymbol>();

            foreach (var ctx in packets)
            {
                var classSymbol = (INamedTypeSymbol) ctx.TargetSymbol;
                var attr = classSymbol.GetAttributes().First(a => a.AttributeClass?.Name == AttributeClassName);
                int id = (int) attr.ConstructorArguments[0].Value;

                if (usedIds.TryGetValue(id, out var existingClassSymbol))
                {
                    var diagnostic = Diagnostic.Create(
                        descriptor: DuplicateIdError,
                        location: ctx.TargetNode.GetLocation(),
                        messageArgs: new object[]{id, existingClassSymbol.Name}
                    );

                    context.ReportDiagnostic(diagnostic);
                    continue;
                }

                usedIds.Add(id, classSymbol);
            }

            sb.AppendLine("// <auto-generated />");
            sb.AppendLine();
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Diagnostics.CodeAnalysis;");
            sb.AppendLine();
            sb.AppendLine($"namespace {AttributeNamespace};");
            sb.AppendLine();
            sb.AppendLine("public static class ServerboundPacketRegistry {");
            sb.AppendLine("    public static int GetPacketId(Serverbound.IServerboundPacket packet) => packet switch");
            sb.AppendLine("    {");

            foreach (var pair in usedIds)
            {
                sb.AppendLine($"        {pair.Value.ToDisplayString()} => {pair.Key},");
            }

            sb.AppendLine("        _ => throw new InvalidOperationException(\"You somehow found a packet without an ID. Did you forget to annotate it accordingly?\")");
            sb.AppendLine("    };");
            sb.AppendLine();
            sb.AppendLine("    public static bool TryCreatePacketById(int id, [NotNullWhen(false)] out Serverbound.IServerboundPacket packet) {");
            sb.AppendLine("        packet = id switch {");

            foreach (var pair in usedIds)
            {
                sb.AppendLine($"            {pair.Key} => new {pair.Value.ToDisplayString()}(),");
            }

            sb.AppendLine("            _ => null");
            sb.AppendLine("        };");
            sb.AppendLine();
            sb.AppendLine("        return packet is not null;");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            context.AddSource("ServerboundPacketRegistry.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
        }

    }

}