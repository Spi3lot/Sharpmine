using System;
using System.Linq;
using System.Text;
using System.Text.Json;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Sharpmine.Gen;

[Generator]
public class PacketsGenerator : IIncrementalGenerator
{

    private const string BaseNamespace = "Sharpmine.Server.Protocol.Packets";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var packetsFile = context.AdditionalTextsProvider
            .Where(static file => file.Path.EndsWith("packets.json", StringComparison.OrdinalIgnoreCase))
            .Select(static (text, cancellationToken) => text.GetText(cancellationToken)?.ToString());

        context.RegisterSourceOutput(packetsFile, Execute);
    }

    private static void Execute(SourceProductionContext context, string? jsonContent)
    {
        if (string.IsNullOrWhiteSpace(jsonContent))
        {
            return;
        }

        using var doc = JsonDocument.Parse(jsonContent);
        var serverboundRegistrySb = new StringBuilder();
        StartRegistry(serverboundRegistrySb);

        foreach (var stateProp in doc.RootElement.EnumerateObject())
        {
            string state = ToPascalCase(stateProp.Name);
            GenerateProtocolDirection(context, stateProp.Value, state, "serverbound", serverboundRegistrySb);
            GenerateProtocolDirection(context, stateProp.Value, state, "clientbound", null);
        }

        EndRegistry(serverboundRegistrySb);
        context.AddSource("ServerboundPacketRegistry.g.cs", SourceText.From(serverboundRegistrySb.ToString(), Encoding.UTF8));
    }

    private static void GenerateProtocolDirection(SourceProductionContext context,
        JsonElement stateElement,
        string protocolState,
        string protocolDirection,
        StringBuilder? registrySb)
    {
        if (!stateElement.TryGetProperty(protocolDirection, out var packetsElement))
        {
            return;
        }

        string directionName = ToPascalCase(protocolDirection);
        string interfaceName = $"I{directionName}Packet";
        string ns = $"{BaseNamespace}.{protocolState}.{directionName}";

        foreach (var packetProp in packetsElement.EnumerateObject())
        {
            int id = packetProp.Value.GetProperty("protocol_id").GetInt32();
            string packetName = packetProp.Name["minecraft:".Length..];
            string className = ToPascalCase(packetName) + "Packet";
            GeneratePacketPartial(context, ns, className, interfaceName, protocolState, protocolDirection, id);
            registrySb?.AppendLine($"        (ProtocolState.{protocolState}, 0x{id:X2}) => new {ns}.{className}(),");
        }
    }

    private static void GeneratePacketPartial(SourceProductionContext context,
        string ns,
        string className,
        string interfaceName,
        string state,
        string direction,
        int id)
    {
        string source = $$"""
                          // <auto-generated />
                          using System;

                          namespace {{ns}};

                          public partial class {{className}} : {{interfaceName}} 
                          {
                              public ProtocolState State => ProtocolState.{{state}};
                              public int Id => 0x{{id:X2}};
                          }
                          """;

        context.AddSource($"{state}/{direction}/{className}.g.cs", SourceText.From(source, Encoding.UTF8));
    }

    private static void StartRegistry(StringBuilder sb)
    {
        sb.AppendLine($$"""
                        // <auto-generated />
                        using System;

                        namespace {{BaseNamespace}};

                        public static class ServerboundPacketRegistry 
                        {
                            public static IServerboundPacket? CreatePacket(ProtocolState state, int id) => (state, id) switch 
                            {
                        """);
    }

    private static void EndRegistry(StringBuilder sb)
    {
        sb.AppendLine("""
                              _ => null
                          };
                      }
                      """);
    }

    private static string ToPascalCase(string input)
    {
        return string.Join("", input.Split(['_', '/'], StringSplitOptions.RemoveEmptyEntries)
            .Select(word => char.ToUpper(word[0]) + word[1..])
        );
    }

}