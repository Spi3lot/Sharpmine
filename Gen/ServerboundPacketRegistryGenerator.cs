using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Sharpmine.Gen
{

    [Generator]
    public class ServerboundPacketRegistryGenerator : IIncrementalGenerator
    {

        private const string TargetNamespace = "Sharpmine.Server.Protocol.Packets";

        private const string TargetClassName = "ServerboundPacketRegistry";

        private const string AttributeNamespace = "Sharpmine.Server.Protocol.Packets";

        private const string AttributeClassName = "PacketAttribute";

        private const string ConnectionState = "ConnectionState";

        private static readonly DiagnosticDescriptor DuplicateIdError = new DiagnosticDescriptor(
            id: "PKT001",
            title: "Duplicate Packet ID",
            messageFormat: "ID 0x{0:X2} for connection state {1} is already used by '{2}'",
            category: "Usage",
            defaultSeverity: DiagnosticSeverity.Error,
            isEnabledByDefault: true
        );

        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var provider = context.SyntaxProvider.ForAttributeWithMetadataName(
                $"{AttributeNamespace}.{AttributeClassName}",
                predicate: (node, _) => node is ClassDeclarationSyntax,
                transform: (ctx, _) => ctx
            );

            context.RegisterSourceOutput(provider.Collect(), Execute);
        }

        private static void Execute(
            SourceProductionContext context,
            ImmutableArray<GeneratorAttributeSyntaxContext> packets
        )
        {
            if (packets.IsDefaultOrEmpty)
            {
                return;
            }

            var sb = new StringBuilder();
            var usedIds = new Dictionary<(int PacketId, byte ConnectionState), INamedTypeSymbol>();

            foreach (var ctx in packets)
            {
                var classSymbol = (INamedTypeSymbol) ctx.TargetSymbol;
                var attr = classSymbol.GetAttributes().First(a => a.AttributeClass?.Name == AttributeClassName);

                int id = (int) attr.ConstructorArguments[0].Value;
                byte connectionState = (byte) attr.ConstructorArguments[1].Value;
                var key = (id, connectionState);

                if (usedIds.TryGetValue(key, out var existingClassSymbol))
                {
                    var diagnostic = Diagnostic.Create(
                        descriptor: DuplicateIdError,
                        location: ctx.TargetNode.GetLocation(),
                        messageArgs: new object[] { id, connectionState, existingClassSymbol.Name }
                    );

                    context.ReportDiagnostic(diagnostic);
                    continue;
                }

                usedIds.Add(key, classSymbol);
            }

            sb.AppendLine("// <auto-generated />");
            sb.AppendLine();
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Diagnostics.CodeAnalysis;");
            sb.AppendLine();
            sb.AppendLine($"namespace {TargetNamespace};");
            sb.AppendLine();
            sb.AppendLine($"public static class {TargetClassName} {{");
            sb.AppendLine($"    public static IServerboundPacket CreatePacket(int id, {ConnectionState} connectionState) => (id, connectionState) switch");
            sb.AppendLine("    {");

            foreach (var pair in usedIds)
            {
                sb.AppendLine($"        ({pair.Key.PacketId}, ({ConnectionState}) {pair.Key.ConnectionState}) => new {pair.Value}(),");
            }

            sb.AppendLine("        _ => null");
            sb.AppendLine("    };");
            sb.AppendLine();
            sb.AppendLine($"    public static (int Id, {ConnectionState} ConnectionState) GetPacketCondition(IServerboundPacket packet) => packet switch");
            sb.AppendLine("    {");

            foreach (var pair in usedIds)
            {
                sb.AppendLine($"        {pair.Value} => ({pair.Key.PacketId}, ({ConnectionState}) {pair.Key.ConnectionState}),");
            }

            sb.AppendLine("        _ => throw new InvalidOperationException(\"You somehow found a packet without a PacketAttribute. Did you forget to annotate it accordingly?\")");
            sb.AppendLine("    };");
            sb.AppendLine("}");
            context.AddSource("ServerboundPacketRegistry.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
        }

    }

}