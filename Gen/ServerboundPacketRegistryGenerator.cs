using System;
using System.Buffers.Binary;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Sharpmine.Gen;

[Generator]
public partial class ServerboundPacketRegistryGenerator : IIncrementalGenerator
{

    [GeneratedRegex("""
                    "minecraft:(.*?)":\s*\{{.*?"protocol_id":\s*(\d+)
                    """)]
    private static partial Regex PacketRegex { get; }

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var packetsFile = context.AdditionalTextsProvider
            .Where(static file => file.Path.Equals("packets.json", StringComparison.OrdinalIgnoreCase))
            .Select(static (text, cancellationToken) => text.GetText(cancellationToken)?.ToString());

        context.RegisterSourceOutput(packetsFile, Execute);
    }

    private static void Execute(SourceProductionContext context, string? jsonContent)
    {
        if (string.IsNullOrWhiteSpace(jsonContent))
        {
            return;
        }

        var packetsByState = ParsePacketsFromJson(jsonContent);
        var registrySb = new StringBuilder();

        registrySb.AppendLine("""
                              // <auto-generated />
                              using System;
                              namespace Sharpmine.Server.Protocol.Packets;

                              public static class ServerboundPacketRegistry 
                              {
                                  public static IServerboundPacket? CreatePacket(int id, ProtocolState state) => (id, state) switch 
                                  {
                              """);

        foreach (var stateKvp in packetsByState)
        {
            string stateName = stateKvp.Key;
            string stateEnum = char.ToUpper(stateName[0]) + stateName[1..];
            string ns = $"Sharpmine.Server.Protocol.Packets.{stateEnum}.Serverbound";

            foreach (var packetKvp in stateKvp.Value)
            {
                string resourceName = packetKvp.Key;
                int protocolId = packetKvp.Value;
                string className = ToPascalCase(resourceName) + "Packet";
                GeneratePacketPartial(context, className);
                registrySb.AppendLine($"        ({protocolId}, ProtocolState.{stateEnum}) => new {ns}.{className}(),");
            }
        }

        registrySb.AppendLine("""
                                      _ => null
                                  };
                              }
                              """);

        context.AddSource("ServerboundPacketRegistry.g.cs", SourceText.From(registrySb.ToString(), Encoding.UTF8));
    }

    private static void GeneratePacketPartial(SourceProductionContext context, string className)
    {
        var sb = new StringBuilder();

        sb.Append("""
                  // <auto-generated />
                  
                  namespace {ns};

                  public partial class {className} : IServerboundPacket 
                  {
                  
                      public int Id => {id};
                      
                      public ProtocolState State => ProtocolState.{stateEnum};
                      
                  }
                  """);

        context.AddSource($"{className}.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
    }

    private static string ToPascalCase(string snakeCase)
    {
        return string.Join("", snakeCase.Split('_')
            .Select(word => char.ToUpper(word[0]) + word[1..])
        );
    }

    private static Dictionary<string, Dictionary<string, int>> ParsePacketsFromJson(string json)
    {
        var result = new Dictionary<string, Dictionary<string, int>>();
        string[] states = ["handshake", "status", "login", "configuration", "play"];

        foreach (string state in states)
        {
            result[state] = [];

            string statePattern = $$"""
                                    "{{state}}":\s*\{[^}]*"serverbound":\s*\{(.*?)\}\s*\,?\s*"clientbound"
                                    """;

            var match = Regex.Match(json, statePattern, RegexOptions.Singleline);

            if (match.Success)
            {
                string serverboundContent = match.Groups[1].Value;
                var packetMatches = PacketRegex.Matches(serverboundContent);

                foreach (var groups in packetMatches.Select(m => m.Groups))
                {
                    result[state][groups[1].Value] = int.Parse(groups[2].Value);
                }
            }
        }

        return result;
    }

}